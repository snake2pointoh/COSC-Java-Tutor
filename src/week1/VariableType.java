package src.week1;

public class VariableType {
    public static void main(String[] args) {
//        byte  (-128 to 127)
//        short
//        int
//        long
//        float (32bits)
//        double (64bits)

//        bool (true false) 1bit

        byte myByte = 0;
        myByte += 200;

        System.out.println(myByte);

        float myFloat = 10.6f;
        double myDouble = 10.6;

        System.out.println(myFloat);
        System.out.println(myDouble);

    }
}
